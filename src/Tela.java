    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String validar;
        boolean valido = false;
        Calculator calc = null;
        try {
            calc = (Calculator) Naming.lookup("//127.0.0.1:1099/CalculatorService");
        } catch (NotBoundException ex) {
            Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(jRadioButton1.isSelected()){
            validar = jFormattedTextField1.getText();
            try {
                valido = calc.RG(validar);
            } catch (RemoteException ex) {
                Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(jRadioButton2.isSelected()){
            validar = jFormattedTextField2.getText();
            try {
                valido = calc.cpf(validar);
            } catch (RemoteException ex) {
                Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(jRadioButton3.isSelected()){
            validar = jFormattedTextField3.getText();
            try {
                valido = calc.cnpj(validar);
            } catch (RemoteException ex) {
                Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            jLabel4.setText("Selecione uma opÃ§Ã£o");
            jLabel4.setVisible(true);
        }
        
        if(valido){
            jLabel4.setText("Valido");
            jLabel4.setVisible(true);
        }
        else{
           jLabel4.setText("Nao valido"); 
            jLabel4.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

